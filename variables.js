//Declara variables globales, var
var saludo = "Hola mundo"
if(saludo) {
    var saludo = "hola"
    console.log(saludo)
}
console.log(saludo)

//let, variables limitadas al bloque donde se define
let nombre = "Josue"
if(nombre) {
    let nombre = "Daniel"
    console.log(nombre)
}
console.log(nombre)

//const, variables que no pueden cambiar
const pi = 3.1416
console.log(pi)
